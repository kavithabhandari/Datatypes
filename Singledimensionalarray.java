package JavaPackage;

public class Singledimensionalarray {
	public static void main(String[] args) {
		int[] arraymylist = new int[4]; // Set size to 4
        arraymylist[0] = 12;
        arraymylist[1] = 13;
        arraymylist[2] = 14;
        arraymylist[3] = 15;
        
        System.out.println(arraymylist[3]); // Outputs 15
        System.out.println(arraymylist.length); // Outputs the size of the array (4)
 
		
	}

}
