package JavaPackage;

public class switchcase {

	public static void main(String[] args) {
		int num1 = 20;
		int num2 = 30;
	int add = num1 + num2;
		switch(add) {
		case 1:
			System.out.println("Result:"+ (num1 + num2));
			break;		case 2:
			System.out.println("Result:" + (num1 - num2));
			break;
			default:
				System.out.println("Error cases failed");
			
			
			
		}
		

	}

}
