package JavaPackage;

public class switchcase {

	public static void main(String[] args) {
		int num1 = 20;
		int num2 = 30;
		int result;
		int add = 1;
		switch(add) {
		case 1:
			result = num1 + num2;
			System.out.println("addition: " + result );
			break;
		case 2:
			result = num1 - num2;
			System.out.println("sub: " + result );
			break;
		case 3:
			result = num1 * num2;
			System.out.println("mul: " + result );
			break;
		case 4:
			result = num1 % num2;
			System.out.println("divide: " + result );
			break;
			
			
			
		}
		

	}

}
